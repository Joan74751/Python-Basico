#Pedir numero al usuario

n1 = int(input("Ingresa un número (1): "))
n2 = int(input("Ingresa un número (2): "))

#Imprimir Resultados

#Suma
print(f'La suma entre {n1} y {n2} es: {n1+n2}')
#Resta
print(f'La resta entre {n1} y {n2} es: {n1-n2}')
#Multiplicacion
print(f'La multiplicacion entre {n1} y {n2} es: {n1*n2}')
#Division
print(f'La division entre {n1} y {n2} es: {n1/n2}')
#Modulo
print(f'El modulo entre {n1} y {n2} es: {n1%n2}')



